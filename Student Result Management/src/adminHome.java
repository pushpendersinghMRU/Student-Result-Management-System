    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course=(String)jComboBox1.getSelectedItem();
        String branch=(String)jComboBox2.getSelectedItem();
        String rollNo=jTextField1.getText();
        String name=jTextField2.getText();
        String sem=(String)jComboBox3.getSelectedItem();
        String fatherName=jTextField3.getText();
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","wordpass");
            Statement st;
            st=conn.createStatement();
            if(rollNo.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Roll Number is empty");
            }
            else
            {
                st.executeUpdate("insert into student(course,branch,rollNo,name,sem,fatherName) values('"+course+"','"+branch+"','"+rollNo+"','"+name+"','"+sem+"','"+fatherName+"')");
                JOptionPane.showMessageDialog(null,"Successfully Updated");
            }
            setVisible(false);
            new adminHome().setVisible(true);
            st.close();
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,"This roll number already exist");
            setVisible(false);
            new adminHome().setVisible(true);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

